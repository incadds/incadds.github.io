	<p><Small>2 September 2016</Small></p></p><h3>	<p>A New Research Study on Personal Experiences of <span class="caps">ADHD</span></p></h3>
                            <p>	<p>A new research study is underway from August-October 2015 on what it is like to have Attention Deficit Hyperactivity Disorder (<span class="caps">ADHD</span>). The &#8216;Better Education, Services, and Treatment for People with Attention Deficit / Hyperactivity Disorder&#8217; Study (<span class="caps">BEST-ADHD</span>), led by researchers at the School of Psychology at <span class="caps">NUI</span> Galway and the Irish National Council for Attention Deficit Hyperactivity Disorder (ADD/<span class="caps">ADHD</span>), is now looking for adult volunteers to take part &#8211; men or women aged 18 or over with <span class="caps">ADD</span> or <span class="caps">ADHD</span> who would like to talk about their experiences of <span class="caps">ADHD</span>, education and health care in an individual research interview. There is also an online survey as an alternative to the interview. </p>

	<p>More details can be found in the study&#8217;s <a href="documents/news/20150815_BEST_Info.pdf">information sheet</a> and <a href="documents/news/20150815_BEST_Fact.pdf">research factsheet</a>.</p>

	<p><Small>15 August 2015</Small></p></p><h3>	<p>Participants Needed for Study on Sleep Problems and GI Symptoms in <span class="caps">ASD</span> Children</p></h3>
                            <p>	<p>A Ph.D. candidate from the National University of Ireland, Galway is seeking parents of autism spectrum disorder (<span class="caps">ASD</span>) children between the ages of 3 to 17 years to participate in a survey to examine sleep problems and gastrointestinal symptoms in children and adolescents with <span class="caps">ASD</span>. Even if your child does not have sleep problems or gastrointestinal symptoms, you are still invited to take part as your information can help inform why these symptoms are present in some children and not in others. For more details see the <a href="/documents/2014_11_NUIG_Sleep_GI.pdf">participant information sheet</a>.</p>

	<p><Small>19 November 2014</Small></p></p><h3>	<p>Participants Needed for Study on Why Adults with <span class="caps">ADHD</span> Are More Likely to Develop Anxiety Disorders</p></h3>
                            <p>	<p>A Ph.D. student from University College Dublin, under the supervision of Dr. Jessica Bramham, is seeking adults with a diagnosis of <br />
<span class="caps">ADHD</span>, anxiety, ADHD+anxiety to take an online questionnaire for a study entitled &#8220;Why are adults with <span class="caps">ADHD</span> more likely to develop anxiety disorders?  An investigation of risk factors, measurement techniques, and impact on neuropsychological functioning&#8221;. The research has been ethically approved by <span class="caps">UCD</span> Office of Research Ethics. For more details see the <a href="documents/2014_11_UCD_PhD_Flyer.pdf">study flyer</a>.</p>

	<p><Small>18 November 2014</Small></p></p><h3>	<p>New Research Shows Autistic Youth Vulnerable to Alcohol Dependence</p></h3>
                            <p>	<p>New research from the Washington University in St. Louis, Missouri has revealed a link between young people with autistic tendencies and alcohol problems. For more details see this <a href="//genetics.wustl.edu/blog/people-with-autistic-tendencies-vulnerable-to-alcohol-problems/">blog post</a> from Washington University&#8217;s Department of Genetics. </p>

	<p>News source &#8211; <a href="//www.thefix.com/content/new-study-shows-autistic-youth-vulnerable-alcohol-dependence">The Fix</a>.</p>

	<p><Small>17 June 2014</Small></p></p><h3>	<p>Participants for Study on <span class="caps">ADHD</span> Diagnoses in Children and Attitudes Towards Trialling Treatment</p></h3>
                            <p>	<p>A final year student at the Dublin Business School (<span class="caps">DBS</span>) is seeking participants for a study that aims to enhance our understanding as to why some parents of <span class="caps">ADHD</span> children choose to trial medication and why others decline. Participants must be a parent of an <span class="caps">ADHD</span> child (under 18 at the time of diagnosis) who was diagnosed between the years 2000 and 2014. Participants are asked to fill an online questionnaire that will take approximately 2-3 minutes to complete. The study has been reviewed and approved by the <span class="caps">DBS</span> ethics board and all responses are anonymous. </p>

	<p>For more details please contact <span class="caps">INCADDS</span>.</p>

